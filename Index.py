example = 'Индексация' # Ввод переменной со строкой
print(example[0]) # Вывод на экран первой буквы
print(example[-1]) # Вывод на экран последней буквы путем отрицательного индекса
print(example[5:]) # Вывод на экран второй половины слова
print(example[::-1]) # Вывод на экран слова наоборот
print(example[1::2]) # Вывод на экран каждой второй буквы нашего слова



